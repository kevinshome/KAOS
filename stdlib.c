char *
itoa(
	int     num)
{
	char    digits[11];
    char *str;
	char *dp;
	char *cp = str;

	if (num == 0) {
		*cp++ = '0';
	} else {
		dp = digits;
		while (num) {
			*dp++ = '0' + num % 10;
			num /= 10;
		}
		while (dp != digits) {
			*cp++ = *--dp;
		}
	}
	*cp++ = '\0';

	return str;
}

int
strcmp(
	const char *s1,
	const char *s2)
{
	unsigned int a, b;

	do {
		a = *s1++;
		b = *s2++;
		if (a != b) {
			return a - b;     /* includes case when
			                   *  'a' is zero and 'b' is not zero
			                   *  or vice versa */
		}
	} while (a != '\0');

	return 0;       /* both are zero */
}